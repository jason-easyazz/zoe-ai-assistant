def _trigger_graphify_rebuild() -> str:
    """Fire-and-forget graphify rebuild. Returns 'started' or an error string."""
    openai_key = os.environ.get("OPENAI_API_KEY", "")
    if not openai_key:
        # Try to read from .env
        env_path = Path("/home/zoe/assistant/.env")
        try:
            for line in env_path.read_text().splitlines():
                if line.startswith("OPENAI_API_KEY="):
                    openai_key = line.split("=", 1)[1].strip().strip('"').strip("'")
                    break
        except Exception:
            pass
    if not openai_key:
        return "skipped (no OPENAI_API_KEY)"
    try:
        env = {**os.environ, "OPENAI_API_KEY": openai_key}
        proc = subprocess.Popen(
            ["graphify", "extract", ".", "--backend", "openai"],
            cwd="/home/zoe/assistant",
            env=env,
            stdout=subprocess.DEVNULL,
            stderr=subprocess.DEVNULL,
            start_new_session=True,
        )
        logger.info("agent_sync: graphify rebuild started (pid=%d)", proc.pid)
        return f"started (pid={proc.pid})"
    except FileNotFoundError:
        return "skipped (graphify not installed)"
    except Exception as exc:
        return f"error: {exc}"


def graphify_search(query: str) -> dict:
    """
    Search the graphify wiki for content matching query.
    Reads from graphify-out/wiki/index.md and returns relevant snippets.
    Called by the MCP graphify_search tool handler.
    """
    wiki_index = _GRAPHIFY_OUT / "wiki" / "index.md"
    if not wiki_index.exists():
        return {
            "error": "Graphify wiki not built yet. Run POST /api/system/agent-sync first.",
            "hint": "After agent-sync, graphify rebuilds in the background (~2min).",
        }

    try:
        index_content = wiki_index.read_text(errors="replace")
    except Exception as exc:
        return {"error": f"Could not read wiki index: {exc}"}

    # Extract wiki file links from the index (lines like: - [Title](filename.md))
    wiki_dir = wiki_index.parent
    link_re = re.compile(r"\[([^\]]+)\]\(([^)]+\.md)\)")
    candidates = []
    q_lower = query.lower()

    for match in link_re.finditer(index_content):
        title, filename = match.group(1), match.group(2)
        if any(kw in title.lower() or kw in filename.lower() for kw in q_lower.split()):
            candidates.append((title, filename))

    # If no title matches, do a content search over all wiki files (capped)
    if not candidates:
        wiki_files = list(wiki_dir.glob("*.md"))[:30]
        for wf in wiki_files:
            if wf.name == "index.md":
                continue
            try:
                text = wf.read_text(errors="replace")
                if q_lower in text.lower():
                    candidates.append((wf.stem, wf.name))
            except Exception:
                pass

    if not candidates:
        return {
            "results": [],
            "message": f"No wiki pages found matching '{query}'. Try broader terms.",
        }

    results = []
    for title, filename in candidates[:3]:
        wiki_file = wiki_dir / filename
        try:
            content = wiki_file.read_text(errors="replace")
            # Return first 800 chars to avoid flooding context
            snippet = content[:800]
            if len(content) > 800:
                snippet += "\n... (truncated)"
            results.append({"title": title, "file": filename, "content": snippet})
        except Exception as exc:
            results.append({"title": title, "file": filename, "error": str(exc)})

    return {"results": results, "query": query, "total_candidates": len(candidates)}


async def run_agent_sync() -> dict:
    """Run the full agent sync. Returns a status dict."""
    mcp_tools, skills, ui_pages = await asyncio.gather(
        _collect_mcp_tools(),
        _collect_openclaw_skills(),
        _collect_ui_pages(),
    )

    zoe_self = _build_zoe_self_md(mcp_tools, skills, ui_pages)
    compact = _build_compact(mcp_tools, skills)
    capabilities = _build_capabilities_md(mcp_tools, skills, ui_pages)

    results: dict = {
        "mcp_tools": len(mcp_tools),
        "skills": len(skills),
        "ui_pages": len(ui_pages),
    }

    # Write ZOE_SELF.md to OpenClaw workspace
    try:
        _OPENCLAW_ZOE_SELF.parent.mkdir(parents=True, exist_ok=True)
        _OPENCLAW_ZOE_SELF.write_text(zoe_self)
        results["openclaw_zoe_self"] = "updated"
        logger.info("agent_sync: wrote ZOE_SELF.md (%d chars) to %s", len(zoe_self), _OPENCLAW_ZOE_SELF)
    except Exception as exc:
        results["openclaw_zoe_self"] = f"error: {exc}"
        logger.warning("agent_sync: ZOE_SELF.md write failed: %s", exc)

    # Patch Hermes SOUL.md
    patched = _patch_hermes_soul(_HERMES_SOUL, zoe_self)
    results["hermes_soul"] = "patched" if patched else "skipped"

    # Write compact
    try:
        _ZOE_COMPACT.parent.mkdir(parents=True, exist_ok=True)
        _ZOE_COMPACT.write_text(compact + "\n")
        results["compact"] = "updated"
        logger.info("agent_sync: wrote compact (%d chars)", len(compact))
    except Exception as exc:
        results["compact"] = f"error: {exc}"
        logger.warning("agent_sync: compact write failed: %s", exc)

    # Write CAPABILITIES.md
    try:
        _CAPABILITIES_MD.write_text(capabilities)
        results["capabilities_md"] = "updated"
        logger.info("agent_sync: wrote CAPABILITIES.md (%d chars)", len(capabilities))
    except Exception as exc:
        results["capabilities_md"] = f"error: {exc}"
        logger.warning("agent_sync: CAPABILITIES.md write failed: %s", exc)

    # Trigger graphify rebuild in background
    graphify_result = _trigger_graphify_rebuild()
    results["graphify_rebuild"] = graphify_result
    logger.info("agent_sync: graphify rebuild: %s", graphify_result)

    results["status"] = "ok"
    return results
