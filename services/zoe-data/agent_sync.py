def _patch_hermes_soul(soul_path: Path, block_content: str, marker: str = "ZOE_SELF") -> bool:
    """Patch a <marker>_BEGIN/END block in SOUL.md. Returns True if patched.

    Used for both the ZOE_SELF (live architecture) and ZOE_WHY (mission/charter)
    blocks so the dreaming-cycle sync regenerates them in place rather than letting
    a hand-edit drift or get clobbered.
    """
    begin, end = f"{marker}_BEGIN", f"{marker}_END"
    try:
        if not soul_path.exists():
            logger.warning("agent_sync: SOUL.md not found at %s", soul_path)
            return False
        content = soul_path.read_text()
        new_block = f"{begin}\n{block_content}\n{end}"
        if begin in content and end in content:
            patched = re.sub(
                re.escape(begin) + r".*?" + re.escape(end),
                lambda _m: new_block,
                content,
                flags=re.DOTALL,
            )
        else:
            patched = content.rstrip() + f"\n\n{new_block}\n"
        soul_path.write_text(patched)
        return True
    except Exception as exc:
        logger.warning("agent_sync: SOUL.md %s patch failed: %s", marker, exc)
        return False


