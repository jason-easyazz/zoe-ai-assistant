"""agent_sync.py — Generate and distribute ZOE_SELF.md to all agents.

Runs as Phase 5 of the dreaming cycle (Sunday 3am) and is also callable via:
  - POST /api/system/agent-sync
  - MCP tool: zoe_sync_knowledge

Generates:
  - ~/.openclaw/workspace/ZOE_SELF.md (full version, capped at 6000 chars)
  - ~/.hermes/SOUL.md (patches the ZOE_SELF_BEGIN/END block)
  - ~/.zoe/zoe_self_compact.txt (compact 500-char version for context prefix)
  - ~/assistant/CAPABILITIES.md (human-readable capability snapshot)

Also triggers a graphify rebuild in the background after sync (requires OPENAI_API_KEY).
"""
import asyncio
import logging
import os
import re
import subprocess
from pathlib import Path

logger = logging.getLogger(__name__)

_OPENCLAW_ZOE_SELF = Path.home() / ".openclaw" / "workspace" / "ZOE_SELF.md"
_HERMES_SOUL = Path.home() / ".hermes" / "SOUL.md"
_ZOE_COMPACT = Path.home() / ".zoe" / "zoe_self_compact.txt"
_CAPABILITIES_MD = Path("/home/zoe/assistant/CAPABILITIES.md")
_GRAPHIFY_OUT = Path("/home/zoe/assistant/graphify-out")
_MAX_ZOE_SELF_CHARS = 6000
_MAX_COMPACT_CHARS = 500


async def _collect_mcp_tools() -> list[str]:
    """Collect MCP tool names from mcp_server.py's _TOOLS list."""
    try:
        from mcp_server import _TOOLS  # type: ignore[import]
        return [t.get("name", "") for t in _TOOLS if t.get("name")]
    except Exception:
        return []


async def _collect_intent_patterns() -> list[str]:
    """Collect intent names from intent_router.py."""
    try:
        import intent_router  # type: ignore[import]
        intents = []
        for attr in dir(intent_router):
            if attr.startswith("Intent") or "_RE" in attr:
                continue
            val = getattr(intent_router, attr, None)
            if callable(val) and attr.startswith("route"):
                intents.append(attr)
        return intents
    except Exception:
        return []


async def _collect_openclaw_skills() -> list[str]:
    """Collect OpenClaw skill names from workspace."""
    skills_dir = Path.home() / ".openclaw" / "workspace" / "skills"
    skills = []
    try:
        if skills_dir.exists():
            for entry in skills_dir.iterdir():
                if entry.is_dir():
                    skills.append(entry.name)
    except Exception:
        pass
    return sorted(skills)


async def _collect_ui_pages() -> list[str]:
    """Collect UI page names from the dist directory."""
    dist_dir = Path("/home/zoe/assistant/services/zoe-ui/dist")
    pages = []
    try:
        if dist_dir.exists():
            for f in dist_dir.glob("*.html"):
                pages.append(f.stem)
    except Exception:
        pass
    return sorted(pages)


