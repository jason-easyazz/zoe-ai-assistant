async def _collect_mcp_tools() -> list[str]:
    """Collect MCP tool names from mcp_server.py's _TOOLS list."""
    try:
        from mcp_server import TOOLS  # type: ignore[import]
        return [t.get("name", "") for t in TOOLS if t.get("name")]
    except Exception:
        return []


async def _collect_intent_patterns() -> list[str]:
    """Collect intent names from intent_router.py."""
    try:
        import intent_router  # type: ignore[import]
        intents = []
        for attr in dir(intent_router):
            if attr.startswith("Intent") or "_RE" in attr:
                continue
            val = getattr(intent_router, attr, None)
            if callable(val) and attr.startswith("route"):
                intents.append(attr)
        return intents
    except Exception:
        return []


async def _collect_openclaw_skills() -> list[str]:
    """Collect OpenClaw skill names from workspace."""
    skills_dir = Path.home() / ".openclaw" / "workspace" / "skills"
    skills = []
    try:
        if skills_dir.exists():
            for entry in skills_dir.iterdir():
                if entry.is_dir():
                    skills.append(entry.name)
    except Exception:
        pass
    return sorted(skills)


async def _collect_ui_pages() -> list[str]:
    """Collect UI page names from the dist directory."""
    dist_dir = Path("/home/zoe/assistant/services/zoe-ui/dist")
    pages = []
    try:
        if dist_dir.exists():
            for f in dist_dir.glob("*.html"):
                pages.append(f.stem)
    except Exception:
        pass
    return sorted(pages)


