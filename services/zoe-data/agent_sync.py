def _build_compact(mcp_tools: list[str], skills: list[str]) -> str:
    """Build a compact single-line summary, capped at _MAX_COMPACT_CHARS."""
    tool_summary = ", ".join(sorted(mcp_tools)[:12]) if mcp_tools else "calendar/reminders/lists/notes/people/HA/memory"
    skill_summary = ", ".join(skills[:5]) if skills else "widget/page/capability-extender"
    compact = (
        f"Zoe: 3-tier AI (Gemma4/Agent :11434, Hermes/GPT-5.4 :8642, OpenClaw/Codex+browser :18789). "
        f"MCP tools: {tool_summary}. Skills: {skill_summary}. "
        f"web_search (DDG), show_map/chart, HA, push, builder skills (admin+staged). "
        f"Check zoe_self_capabilities MCP. Full: ZOE_SELF.md."
    )
    if len(compact) > _MAX_COMPACT_CHARS:
        compact = compact[:_MAX_COMPACT_CHARS - 3] + "..."
    return compact


def _patch_hermes_soul(soul_path: Path, zoe_self_content: str) -> bool:
    """Patch the ZOE_SELF_BEGIN/END block in SOUL.md. Returns True if patched."""
    try:
        if not soul_path.exists():
            logger.warning("agent_sync: SOUL.md not found at %s", soul_path)
            return False
        content = soul_path.read_text()
        new_block = f"ZOE_SELF_BEGIN\n{zoe_self_content}\nZOE_SELF_END"
        if "ZOE_SELF_BEGIN" in content and "ZOE_SELF_END" in content:
            patched = re.sub(
                r"ZOE_SELF_BEGIN.*?ZOE_SELF_END",
                new_block,
                content,
                flags=re.DOTALL,
            )
        else:
            patched = content.rstrip() + f"\n\n{new_block}\n"
        soul_path.write_text(patched)
        return True
    except Exception as exc:
        logger.warning("agent_sync: SOUL.md patch failed: %s", exc)
        return False


