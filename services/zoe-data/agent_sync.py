def _env_enabled(name: str, default: bool = False) -> bool:
    value = os.environ.get(name)
    if value is None:
        return default
    return value.strip().lower() in {"1", "true", "yes", "on"}


async def run_agent_sync() -> dict:
    """Run the full agent sync. Returns a status dict."""
    mcp_tools, skills, ui_pages = await asyncio.gather(
        _collect_mcp_tools(),
        _collect_openclaw_skills(),
        _collect_ui_pages(),
    )

    zoe_self = _build_zoe_self_md(mcp_tools, skills, ui_pages)
    compact = _build_compact(mcp_tools, skills)
    capabilities = _build_capabilities_md(mcp_tools, skills, ui_pages)

    results: dict = {
        "mcp_tools": len(mcp_tools),
        "skills": len(skills),
        "ui_pages": len(ui_pages),
    }

    # Write ZOE_SELF.md to OpenClaw workspace
    try:
        _OPENCLAW_ZOE_SELF.parent.mkdir(parents=True, exist_ok=True)
        _OPENCLAW_ZOE_SELF.write_text(zoe_self)
        results["openclaw_zoe_self"] = "updated"
        logger.info("agent_sync: wrote ZOE_SELF.md (%d chars) to %s", len(zoe_self), _OPENCLAW_ZOE_SELF)
    except Exception as exc:
        results["openclaw_zoe_self"] = f"error: {exc}"
        logger.warning("agent_sync: ZOE_SELF.md write failed: %s", exc)

    # Patch Hermes SOUL.md — live architecture (ZOE_SELF) + mission/charter (ZOE_WHY)
    patched = _patch_hermes_soul(_HERMES_SOUL, zoe_self, marker="ZOE_SELF")
    results["hermes_soul"] = "patched" if patched else "skipped"

    zoe_why = _build_zoe_why()
    patched_why = _patch_hermes_soul(_HERMES_SOUL, zoe_why, marker="ZOE_WHY")
    results["hermes_why"] = "patched" if patched_why else "skipped"

    # Write compact
    try:
        _ZOE_COMPACT.parent.mkdir(parents=True, exist_ok=True)
        _ZOE_COMPACT.write_text(compact + "\n")
        results["compact"] = "updated"
        logger.info("agent_sync: wrote compact (%d chars)", len(compact))
    except Exception as exc:
        results["compact"] = f"error: {exc}"
        logger.warning("agent_sync: compact write failed: %s", exc)

    # Write CAPABILITIES.md
    try:
        _CAPABILITIES_MD.write_text(capabilities)
        results["capabilities_md"] = "updated"
        logger.info("agent_sync: wrote CAPABILITIES.md (%d chars)", len(capabilities))
    except Exception as exc:
        results["capabilities_md"] = f"error: {exc}"
        logger.warning("agent_sync: CAPABILITIES.md write failed: %s", exc)


    results["status"] = "ok"
    return results
